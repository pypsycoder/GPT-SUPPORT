# get_education_list
# Эндпоинт для получения списка обучающих материалов.
@router.get("/list", response_model=List[EducationItem])
async def get_education_list(
    topic: Optional[str] = Query(
        default=None,
        description="Фильтр по теме материала (например, 'stress', 'sleep', 'coping').",
    ),
):
    """
    Вернуть список обучающих материалов.

    Если передан параметр topic, вернёт только материалы по этой теме.
    Темы используются как технические ключи:
    - "stress"  – стресс и напряжение,
    - "sleep"   – сон и восстановление,
    - "coping"  – копинг-стратегии и опоры.

    Фронтенд (education.html) группирует материалы по topic
    и отображает их блоками.
    """
    # без фильтра — возвращаем всё
    if not topic:
        return EDUCATION_ITEMS

    topic_lower = topic.lower()
    filtered = [item for item in EDUCATION_ITEMS if item.topic.lower() == topic_lower]
    return filtered
